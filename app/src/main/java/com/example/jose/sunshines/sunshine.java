    @Override
    public void onLoadFinished(Loader<ArrayList<ListObject>> loader, ArrayList<ListObject> data) {
        ListView listView = (ListView) findViewById(R.id.ListPrincipal) ;
        wordadapter adapter = new wordadapter(this ,data  );
        listView.setAdapter(adapter );
        final ArrayList<ListObject> botondata = data ;

        TextView TxtFecha = (TextView)findViewById(R.id.TxtFecha);
        TextView TxtGrados = (TextView)findViewById(R.id.TxtGrados);
        TextView TxtGrados2 = (TextView)findViewById(R.id.TxtGrados2);
        TextView TxtTiempo = (TextView)findViewById(R.id.TxtTiempo);


        ListObject texto = botondata.get(0);
        TxtFecha.setText(texto.getDay());
        TxtGrados.setText(texto.getGradosmax());
        TxtGrados2.setText(texto.getGradosmax());
        TxtTiempo.setText(texto.getTime());



        listView.setOnItemClickListener(new AdapterView.OnItemClickListener() {
            @Override
            public void onItemClick(AdapterView<?> parent, View view, int position,
                                    long id) {
                Intent intent = new Intent(view.getContext(), informacion.class);
                Bundle extra = new Bundle();
                ListObject word = botondata.get(position) ;
                Bundle b = new Bundle();
                b.putSerializable("bundle", word);
                intent.putExtras(b);
                startActivity(intent);
            }
        });



    }

